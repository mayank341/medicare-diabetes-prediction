from fastapi import FastAPI
from pydantic import BaseModel
import pickle 
import json 



app=FastAPI()

class user_input(BaseModel):
    Pregnancies:int
    Glucose:int
    BloodPressure:int
    SkinThickness:int
    Insulin:int
    BMI:float
    DaibetesPedigreeFunction:float
    Age:int


#  loading the model :
diabetic_model=pickle.load(open("diabetes_model.pkl",'rb'))

    
@app.get("/")
def home():
    return{"messages":"Daibetes Prediction "}

@app.post("/diabetes_prediction")
def diabetes_predict(input_parameters:user_input):

    input_data=input_parameters.json()
    input_dictionary=json.loads(input_data)

    preg=input_dictionary['Pregnancies']
    glu=input_dictionary['Glucose']
    bp=input_dictionary['BloodPressure']
    skin=input_dictionary['SkinThickness']
    insulin=input_dictionary['Insulin']
    bmi=input_dictionary['BMI']
    dof=input_dictionary['DaibetesPedigreeFunction']
    age=input_dictionary['Age']

    input_list=[preg,glu,bp,skin,insulin,bmi,dof,age]

    prediction=diabetic_model.predict(input_list)

